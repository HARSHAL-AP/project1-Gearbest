function displaymendata(appliancesData)
{
  document.querySelector("#container").innerText = "";

  appliancesData.map(function(ele){
    var box = document.createElement("div")
    box.setAttribute("class","box")
 
    var image = document.createElement("img")
    image.src = ele.image_url
    image.setAttribute("class","image")
 
    var name = document.createElement("p")
    name.innerText = ele.name
    name.setAttribute("class","name")
 
    var box2 = document.createElement("div")
    box2.setAttribute("class","innerdiv")
 
    var strprice = document.createElement("p")
    strprice.innerText =`$${ele.strikedoffprice}`
    strprice.style.textDecoration = "line-through"
    strprice.setAttribute("class","strprice")
   
    var price = document.createElement("p")
    price.innerText = `$${ele.price}`
    price.setAttribute("class","price")
 
    var off = document.createElement("div")
    off.innerText = ele.offprice
    off.setAttribute("class","offdiv")

    var box3 = document.createElement("div")
    box3.setAttribute("class","innerdiv1")
 
    var ratings = document.createElement("img")
    ratings.src = ele.ratingimg
    ratings.setAttribute("class","rating")

    var rate = document.createElement("p")
    rate.innerText = ele.rate
    rate.setAttribute("class","rate")
 
    var btn = document.createElement("button")
    btn.innerText = "ADD TO CART"
    btn.addEventListener("click", function(){
            menCart(ele);
        })
 
    box.append(image,name,box2,box3,btn)
    box2.append(strprice,price,off)
    box3.append(ratings,rate)
    document.querySelector("#container").append(box)
 })
 
 function menCart(ele){
     console.log(menCart)
     cartdata.push(ele)
     localStorage.setItem("cartitems",JSON.stringify(cartdata))
     alert("Item Added To The Cart")
     window.location.href = "cart.html"
 
 }
}
